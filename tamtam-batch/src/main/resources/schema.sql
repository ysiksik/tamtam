ALTER TABLE tour_attractions ADD COLUMN IF NOT EXISTS detail_common_synced_at TIMESTAMP;
ALTER TABLE tour_attractions ADD COLUMN IF NOT EXISTS detail_image_source_modified_time VARCHAR(30);
ALTER TABLE tour_attractions ADD COLUMN IF NOT EXISTS detail_image_synced_at TIMESTAMP;
